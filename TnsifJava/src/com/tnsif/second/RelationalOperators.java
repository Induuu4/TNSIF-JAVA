package com.tnsif.second;

public class RelationalOperators {
		  public static void main(String[] args) {
		    int a = 7; 
		    int b = 11;
		    System.out.println("a is " + a + " and b is " + b);
		    System.out.println(a == b);  // false
		    System.out.println(a != b);  // true
		    System.out.println(a > b);  // false
		    System.out.println(a < b);  // true
		    System.out.println(a >= b);  // false
		    System.out.println(a <= b);  // true
		  }
}