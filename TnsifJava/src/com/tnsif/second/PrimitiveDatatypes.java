package com.tnsif.second;

public class PrimitiveDatatypes {
	public static void main(String args[]) {
		byte Byte =101;
		short Short =20000;
		int Integer =1000;
		long Long = 1234567;
		float Float=23.233f;
		double Double=34.444444444;
		char Char = 'H';
		boolean bool = true;
		System.out.println("bytevalue="+Byte);
		System.out.println("Shortvalue="+Short);
		System.out.println("IntValue="+Integer);
		System.out.println("LongValue="+Long);
		System.out.println("FloatValue="+Float);
		System.out.println("DoubleValue="+Double);
		System.out.println("Charvalue="+Char);
		System.out.println("BooleanValue="+bool);
	}

}